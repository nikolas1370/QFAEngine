Skybox::Skybox()
{
	/*    vertex Buffer    */
//GLuint vertexBuffer; // unique id all in opengl have it
/* buffer in gpu */
	GLCall(glGenBuffers(1, &VBO));
	GLCall(glBindBuffer(GL_ARRAY_BUFFER, VBO));// bind for future render(glDrawElements)
	// next is paraters in vertex shader // what order data in

	GLCall(glBufferData(GL_ARRAY_BUFFER, sizeof(Skybox::skyboxVertices), &Skybox::skyboxVertices, GL_STATIC_DRAW));
	GLCall(glBindBuffer(GL_ARRAY_BUFFER, 0));
	glEnableVertexAttribArray(0);
	glVertexAttribPointer(0, 3, GL_FLOAT, GL_FALSE, sizeof(float) * 3, 0);
}

void Skybox::Bind()
{
	GLCall(glBindBuffer(GL_ARRAY_BUFFER, VBO));
}
